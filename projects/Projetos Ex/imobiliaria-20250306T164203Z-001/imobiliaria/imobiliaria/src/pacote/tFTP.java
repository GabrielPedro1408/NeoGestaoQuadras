    private void btEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEnviarActionPerformed
        int tam;
        tam = lstNome.getModel().getSize()-1;
        //Rotina de inclusão de imagens no B.D
        try{
            for(int i = 0; i<=tam; i++){
                String sql = "insert into imagens(id_imovel, imagem) values (" +  idImovel + ",'" + idImovel+"_"+listaNome.getElementAt(i).toString()+"')";
                PreparedStatement stm = con.prepareStatement(sql);
                stm.executeUpdate();
            }
            
        } catch(SQLException erro){
            JOptionPane.showMessageDialog(null, "Erro ao incluir as imagens! " +erro);
        }
        //Rotina para guardar o arquivo de imagem em um servidor
        try{
            FTPClient ftp = new FTPClient();
            ftp.connect("127.0.0.1");
            ftp.login("Vitor","1007");
            ftp.setFileType(FTPClient.BINARY_FILE_TYPE);
            
            ftp.makeDirectory(idImovel);
            ftp.changeWorkingDirectory(idImovel);
            
            for(int i = 0; i<=tam; i++){
                FileInputStream arqEnviar = new FileInputStream(listaCaminho.getElementAt(i).toString());
                if(ftp.storeFile(idImovel+"_"+listaNome.getElementAt(i),arqEnviar)){
                    JOptionPane.showMessageDialog(null, "Arquivo armazenado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao aramazenar o arquivo!");
                }
            }
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Erro! " + e);
        }
    }//GEN-LAST:event_btEnviarActionPerformed

