    private void btProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarActionPerformed
        //Aqui começa a rotina de selecionar os arquivos usando o fileChoser
        File arquivo;
        String nomeArq;
        BufferedImage imagem;
        FC.setFileFilter(new FileNameExtensionFilter("Image files", "png", "jpg"));
        FC.setAcceptAllFileFilterUsed(false);
        FC.showOpenDialog(null);
        arquivo = FC.getSelectedFile();
        nomeArq = FC.getName(arquivo);
        tfNomeArq.setText(nomeArq);
        tfCaminho.setText(arquivo.toString());
        
        try{
            imagem = ImageIO.read(arquivo);
            ImageIcon icon = new ImageIcon(imagem.getScaledInstance(400, 400, java.awt.Image.SCALE_SMOOTH));
            lb1.setIcon(icon);
            
            
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Erro!" +e);
        }
    }//GEN-LAST:event_btProcurarActionPerformed

